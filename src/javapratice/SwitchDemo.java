package javapratice;

public class SwitchDemo {

	public static void main(String[] args) {
		int n=10;
		switch(n)
	{
		case 10:
		System.out.println("Iam 10");
		break;
		
		case 20:
		System.out.println("Iam 20");
		
		case 30:
		System.out.println("Iam 30");
		break;
		
		default:
		System.out.println("this code will execute if no one matches");
	}
		System.out.println("this code blong to main method");
	}

}
