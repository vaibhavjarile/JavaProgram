package javapratice;

public class Pattern {

	public static void main(String[] args) {
		
		for(int i=1; i<=10; i++)
	{
			for(int v=1; v<=i; v++ )
			{
				System.out.print(" $ ");
			}
					System.out.println();
				}
			for(int i=1; i<=10; i++)
			{
				for(int v=4; v>=i; v--)
				{
					System.out.print(" $ ");
				}
				{
					System.out.println();
				}
			}
	}
	}
	

