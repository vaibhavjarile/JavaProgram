package wrapperClasses;

public class Wrapper {

	public static void main(String[] args) {
		int i=10;                              //Primitive datatype to Wrapper Classes
		Integer I = Integer.valueOf(i);        //int to Integer
				System.out.println(I);
		char c='v';
		Character C= Character.valueOf(c);     //char to Character
				System.out.println(C);
		float f=10.0f;                           
		Float F=Float.valueOf(f);             //float to Float
				System.out.println(F);
	
	
	
	}

}
